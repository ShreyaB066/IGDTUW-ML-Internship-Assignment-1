# Write a python program that counts the occurrences of a specific element in a list.
list = ["Hi",1,"All",1,"Hello","Hello"] 
freq_of_1 = list.count(1)
freq_of_Hi = list.count("Hi")
freq_of_All = list.count("All")
freq_of_Hello = list.count("Hello")
print("Frequency of 1:",freq_of_1)
print("Frequency of Hi:",freq_of_Hi)
print("Frequency of All:",freq_of_All)
print("Frequency of Hello:",freq_of_Hello)
